#include <stdio.h>
#include <stdlib.h>
/**
 * main - a function that print multiplication of two int
 * @argc: number of arguments
 * @argv: pointer of arguments
 * Return: alway 1
 */
int main(int argc, char *argv[])
{
	int i, j, sum = 0;

	for (i = 1; i < argc; i++)
	{
		for (j = 0; argv[i][j]; j++)
		{
			while (argv[i][j] < '0' || argv[i][j] > '9')
			{
				printf("Error\n");
				return (1);
			}
		}

		sum += atoi(argv[i]);
	}

	printf("%d\n", sum);

	return (0);
}
